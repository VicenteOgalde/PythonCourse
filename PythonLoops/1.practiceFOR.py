# Loops are used to repeat a statement or block of code
# Loops can be determinate or indeterminate
# The difference being that with a determinate loop structure, one can (normally) predict exactly how many times the loop will repeat; where as with an indeterminate loop structure, this is not always the case. An indeterminate loop structure loops as long as a condition evaluates to some certain boolean value.

for i in [1,2,3]: #for i in [] loop through the data structure list,tuple,etc
    print("hello")
    print(i)

